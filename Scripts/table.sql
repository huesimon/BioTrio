DROP SCHEMA public CASCADE;
CREATE SCHEMA public;
CREATE TABLE movie
( movie_id SERIAL,
 title varchar(100) NOT NULL,
 length varchar(30),
 PRIMARY KEY (movie_id)
 );
  CREATE TABLE hall
 (hall_id SERIAL,
 rowCount integer,
 rowLength integer,
 totalSeats integer,
 name varchar(20),
  PRIMARY KEY(hall_id)
 );
CREATE TABLE customer
 ( customer_id SERIAL,
 name varchar(50) NOT NULL,
 phoneNr varchar(20),
 PRIMARY KEY (customer_id) 
 );
  CREATE TABLE orders
 (order_id SERIAL,
 customer integer references customer(customer_id),
  PRIMARY KEY (order_id)
 );
  CREATE TABLE showing
 (showing_id SERIAL,
  movie integer REFERENCES movie(movie_id) NOT NULL,
  hall integer REFERENCES hall(hall_id) NOT NULL,
  date varchar(50),
  PRIMARY KEY(showing_id)
 );
  CREATE TABLE ticket
 (ticket_id SERIAL,
 rowNo integer NOT NULL,
 seatNo integer NOT NULL,
 orders integer references orders(order_id)NOT NULL,
  showing integer REFERENCES showing(showing_id)NOT NULL,
  PRIMARY KEY(ticket_id)
 );


  
